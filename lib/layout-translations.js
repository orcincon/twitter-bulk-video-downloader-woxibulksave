const SUPPORTED_LANGS = ['en', 'tr', 'de', 'es'];

function resolveLayoutLang(urlLang, acceptLanguage) {
  if (urlLang && SUPPORTED_LANGS.includes(urlLang)) return urlLang;
  if (!acceptLanguage) return 'en';
  const parts = acceptLanguage.split(',').map((s) => s.split(';')[0].trim().slice(0, 2).toLowerCase());
  return parts.find((l) => SUPPORTED_LANGS.includes(l)) || 'en';
}

export function getLayoutTranslations(urlLang, acceptLanguage) {
  const lang = resolveLayoutLang(urlLang, acceptLanguage);
  return layoutTranslations[lang] || layoutTranslations.en;
}

export function getResolvedLang(urlLang, acceptLanguage) {
  return resolveLayoutLang(urlLang, acceptLanguage);
}

/** URL yoksa giriş yapan kullanıcının Supabase'deki dil tercihini kullan */
export async function getResolvedLangWithPreference(urlLang, acceptLanguage, session) {
  if (urlLang && SUPPORTED_LANGS.includes(urlLang)) return urlLang;
  const userId = session?.user?.id || session?.user?.email;
  if (userId) {
    try {
      const { createSupabaseClient, ensureUserInSupabase } = await import('./supabase.js');
      if (session?.user?.id) await ensureUserInSupabase(session);
      const supabase = createSupabaseClient();
      if (supabase) {
        const { data } = await supabase.from('users').select('preferred_language').eq('id', userId).single();
        if (data?.preferred_language && SUPPORTED_LANGS.includes(data.preferred_language)) {
          return data.preferred_language;
        }
      }
    } catch (_) {}
  }
  return resolveLayoutLang(urlLang, acceptLanguage);
}
